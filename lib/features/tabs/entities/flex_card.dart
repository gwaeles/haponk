class FlexCard {
  final int id;
  final int tabId;
  final String type;
  final int stateId;
  final int parentId;
  final int horizontalPosition;
  final int verticalPosition;
  final int horizontalFlex;
  final int verticalFlex;
  final int width;
  final int height;

  FlexCard({
